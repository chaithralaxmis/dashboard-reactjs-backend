const mongoose = require("mongoose");

const enqSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  mobile: {
    type: Number,
    required: true,
  },
  comment: {
    type: String,
    required: true,
  },
  status: {
    type: String,
    default: "Submitted",
    enum: ["Submitted", "Contacted", "In progress", "Solved"],
  },
});

const Enquiry = mongoose.model("enquiry", enqSchema);
module.exports = Enquiry;
